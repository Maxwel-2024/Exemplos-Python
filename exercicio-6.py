choveu_hoje = False
print(choveu_hoje)
print(type(choveu_hoje))  # Verifica se é um valor booleano (bool) ou seja se é True(verdadeiro) ou False(falso)

numero = 10
print(numero)
print(type(numero))  # Verifica se é um número inteiro (int)

numero = "dez"
print(numero)
print(type(numero))  # Verifica se agora é uma string (str)